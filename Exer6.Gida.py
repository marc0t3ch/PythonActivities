# A python Program that will convert kilometer to mile
# This line of code below will take kilometer/s input from the user
kilometers = float(input("Enter value in kilometers: "))
# This line of code below is the conversion factor
factorConversion = 0.621371
# This line of code below will calculate miles
miles = kilometers * factorConversion
print('%0.2f kilometers is equal to %0.2f miles' %(kilometers,miles))
